/**
 * Доменные типы FloorCalc.
 *
 * Convention: все геометрические размеры хранятся как **integer mm**
 * (миллиметры, целые), чтобы исключить накопление floating-point ошибок.
 * Преобразование в метры — только на UI-слое (см. units.ts).
 *
 * Convention направления: все полосы укладываются вдоль `room.length`
 * (длина рулона == длина полосы). `room.width` — короткая сторона помещения,
 * поперёк рулона. `roll.width` — ширина рулона (полосы), `roll.length` — длина.
 */

export type RollType = {
  /** Уникальный идентификатор типоразмера рулона. */
  id: string;
  /** Ширина рулона, mm (integer). */
  width: number;
  /** Длина рулона, mm (integer). */
  length: number;
};

export type Room = {
  /** Уникальный идентификатор помещения. */
  id: string;
  /** Имя для UI (например, «Комната 1»). */
  name: string;
  /** Ширина помещения (короткая сторона, поперёк рулона), mm (integer).
   *  Для layout='rect' — задаётся пользователем; для layout='free' —
   *  derived из shape.bboxWidth (см. computeBBoxFromShape). */
  width: number;
  /** Длина помещения (длинная сторона, вдоль рулона), mm (integer).
   *  Для layout='rect' — задаётся пользователем; для layout='free' —
   *  derived из shape.bboxLength. */
  length: number;
  /** Режим задания геометрии. Default: 'rect' (текущее поведение). */
  layout?: RoomLayout;
  /** Свободная планировка: ортогональный полигон. Присутствует если layout='free'. */
  shape?: RoomShape;
};

export type RoomLayout = 'rect' | 'free';

/**
 * Узел сетки (целочисленные координаты на сетке 0..GRID_MAX, GRID_MAX = 4 для 5×5 точек).
 * Сетка используется ТОЛЬКО для топологии (UX-удобство клика).
 * Реальные размеры стен задаются отдельно в Wall.lengthMm.
 */
export type GridPoint = {
  /** Колонка сетки, 0..GRID_MAX. */
  col: number;
  /** Строка сетки, 0..GRID_MAX. */
  row: number;
};

/**
 * Ортогональный сегмент стены между двумя соседними точками сетки.
 * Сегмент строго горизонтальный или вертикальный (никаких диагоналей).
 */
export type Wall = {
  /** Стартовая точка сетки (топология). */
  from: GridPoint;
  /** Конечная точка сетки. Соседняя с from по одной из осей (Manhattan-distance = 1). */
  to: GridPoint;
  /** Реальная длина стены в mm. null = пользователь ещё не ввёл размер. */
  lengthMm: number | null;
};

/**
 * Свободная (ортогональная) планировка помещения.
 *
 * Контракты:
 *  1. Все стены ортогональны (горизонтальные или вертикальные).
 *  2. walls образуют ЗАМКНУТЫЙ контур: каждая внутренняя точка соединения
 *     имеет ровно 2 соседних стены (полигон без ответвлений).
 *  3. Полигон НЕ самопересекается (контракт UI; домен не валидирует автоматически).
 *  4. Совместимость размеров: суммы lengthMm горизонтальных и вертикальных стен
 *     согласованы по «правилу замкнутости» (суммы стен в направлениях слева→направо
 *     и справа→налево равны; аналогично для вертикалей). При нарушении — bbox
 *     вычисляется по walk-алгоритму с допуском.
 */
export type RoomShape = {
  /** Список стен в порядке обхода полигона (CW или CCW — не важно). */
  walls: Wall[];
};

export type Mode = 'economy' | 'optimal';

/**
 * Один уложенный кусок покрытия.
 * source*: координаты выреза в исходном рулоне (если бы рулон лежал плашмя).
 * placedAt*: координаты в помещении (0,0 — левый верх).
 */
export type Piece = {
  /** Индекс рулона из числа использованных в этом расчёте (0..rollsUsed-1). */
  rollIndex: number;
  /** Тип рулона, из которого вырезан кусок. */
  rollTypeId: string;
  /** Координата X выреза в исходном рулоне, mm. */
  sourceX: number;
  /** Координата Y выреза в исходном рулоне, mm. */
  sourceY: number;
  /** Ширина куска, mm (в комнате: ось X, placedAtX..placedAtX+width). */
  width: number;
  /** Длина куска, mm (в комнате: ось Y, placedAtY..placedAtY+length). */
  length: number;
  /** Координата X в помещении (0..room.width), mm. */
  placedAtX: number;
  /** Координата Y в помещении (0..room.length), mm. */
  placedAtY: number;
  /**
   * Если `rotated=true`, кусок повёрнут на 90° относительно исходной ориентации
   * рулона. В исходном рулоне был вырезан прямоугольник размером
   * `[sourceX..sourceX+length] × [sourceY..sourceY+width]` (ширина и длина свапнуты),
   * а в комнате он лежит как `width × length` (length по оси Y, width по оси X).
   * При `rotated=false` (по умолчанию): вырез `[sourceX..sourceX+width] × [sourceY..sourceY+length]`.
   */
  rotated?: boolean;
};

/**
 * Прямоугольный обрезок в банке. Координаты — внутри исходного рулона.
 */
export type Offcut = {
  /** Индекс исходного рулона. */
  rollIndex: number;
  /** Координата X прямоугольника в рулоне, mm. */
  sourceX: number;
  /** Координата Y прямоугольника в рулоне, mm. */
  sourceY: number;
  /** Ширина обрезка, mm. */
  width: number;
  /** Длина обрезка, mm. */
  length: number;
};

export type CalculationResult = {
  mode: Mode;
  roomId: string;
  rollTypeId: string;
  /** Сколько целых рулонов использовано для покрытия помещения. */
  rollsUsed: number;
  /** Количество дискретных швов (продольных + поперечных). */
  seamCount: number;
  /** Все уложенные куски в порядке их размещения. */
  pieces: Piece[];
  /** Площадь обрезков (то, что ушло в waste / банк), mm². */
  wasteAreaMm2: number;
  /** Текстовые предупреждения для пользователя (например, «помещение длиннее рулона»). */
  warnings: string[];
  /** false — расчёт невозможен (помещение не задано / геометрия некорректна). */
  feasible: boolean;
};
